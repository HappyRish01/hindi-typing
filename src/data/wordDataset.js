// Get word pool based on level with smooth progression
export const getWordPoolForLevel = (level) => {
  if (level <= 2) {
    // Level 1-2: Only easy words
    return EASY_WORDS;
  } else if (level <= 4) {
    // Level 3-4: Mostly easy, some medium
    return [...EASY_WORDS, ...MEDIUM_WORDS.slice(0, Math.floor(MEDIUM_WORDS.length / 2))];
  } else if (level <= 6) {
    // Level 5-6: Easy and medium words
    return [...EASY_WORDS, ...MEDIUM_WORDS];
  } else if (level <= 8) {
    // Level 7-8: Medium and some hard words
    return [...MEDIUM_WORDS, ...HARD_WORDS.slice(0, Math.floor(HARD_WORDS.length / 2))];
  } else {
    // Level 9+: All words with emphasis on hard
    return [...MEDIUM_WORDS.slice(Math.floor(MEDIUM_WORDS.length / 2)), ...HARD_WORDS];
  }
};

// Get random word from pool (avoiding recent words)
export const getRandomWord = (wordPool, recentWords = []) => {
  const availableWords = wordPool.filter(w => !recentWords.includes(w));
  const pool = availableWords.length > 0 ? availableWords : wordPool;
  return pool[Math.floor(Math.random() * pool.length)];
};

